    // 📝 Парсинг ответа
    private function parseAIResponse(string $text, string $language): array
    {
        $text = preg_replace('/```json\s*/i', '', $text);
        $text = preg_replace('/```\s*/', '', $text);
        $text = trim($text);

        if (preg_match('/\{[\s\S]*\}/', $text, $matches)) {
            $json = json_decode($matches[0], true);
            if ($json && json_last_error() === JSON_ERROR_NONE) {
                return [
                    'number' => $json['number'] ?? '№ ' . rand(100, 999),
                    'type' => $json['type'] ?? ($language === 'tj' ? 'Ҳуҷҷат' : 'Документ'),
                    'title' => $json['title'] ?? ($language === 'tj' ? 'Ҳуҷҷати эҷодшуда' : 'Сгенерированный документ'),
                    'content' => $json['content'] ?? $text,
                    'deadline' => $json['deadline'] ?? date('Y-m-d', strtotime('+30 days')),
                    'status' => $json['status'] ?? 'draft'
                ];
            }
        }

        return [
            'number' => '№ ' . rand(100, 999),
            'type' => $language === 'tj' ? 'Ҳуҷҷат' : 'Документ',
            'title' => $language === 'tj' ? 'Ҳуҷҷати эҷодшуда' : 'Сгенерированный документ',
            'content' => $text,
            'deadline' => date('Y-m-d', strtotime('+30 days')),
            'status' => 'draft'
        ];
    }

