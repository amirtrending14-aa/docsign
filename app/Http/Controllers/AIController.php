    // ==================== ГЕНЕРАЦИЯ ФАЙЛОВ ====================

    private function generateFile(array $data, string $format): string
    {
        $fileName = 'ai_doc_' . time() . '_' . uniqid();

        if ($format === 'word') {
            return $this->generateWord($data, $fileName);
        } else {
            return $this->generatePdf($data, $fileName);
        }
    }

    private function generateWord(array $data, string $fileName): string
    {
        $phpWord = new PhpWord();

        $phpWord->addTitleStyle(1, [
            'name' => 'Arial',
            'size' => 18,
            'bold' => true,
            'color' => '1A365D'
        ], ['alignment' => 'center', 'spaceAfter' => 200]);

        $section = $phpWord->addSection([
            'paperSize' => 'A4',
            'marginLeft' => 1418,
            'marginRight' => 1418,
            'marginTop' => 1134,
            'marginBottom' => 1134,
        ]);

        $section->addTitle($data['title'] ?? 'Документ', 1);

        $metaText = "Номер: " . ($data['number'] ?? 'Б/Н') . "  |  " .
            "Тип: " . ($data['type'] ?? 'Документ') . "  |  " .
            "Дата: " . date('d.m.Y');

        $section->addText($metaText, [
            'name' => 'Arial',
            'size' => 10,
            'color' => '718096',
            'italic' => true
        ], ['alignment' => 'center']);

        $section->addTextBreak(1);

        $content = $data['content'] ?? '';
        $paragraphs = preg_split('/\n\s*\n/', $content);

        foreach ($paragraphs as $paragraph) {
            $paragraph = trim($paragraph);
            if (empty($paragraph)) continue;

            if (preg_match('/^[1-9]\.\s+/u', $paragraph) ||
                preg_match('/^(Глава|Раздел|Пункт|Статья|Chapter|Section)/iu', $paragraph)) {
                $section->addText($paragraph, [
                    'name' => 'Arial',
                    'size' => 12,
                    'bold' => true,
                    'color' => '2D3748'
                ], ['spaceBefore' => 200, 'spaceAfter' => 100]);
            } else {
                $section->addText($paragraph, [
                    'name' => 'Arial',
                    'size' => 11,
                    'color' => '2D3748'
                ], ['alignment' => 'both', 'spaceAfter' => 120]);
            }
        }

        $filePath = 'documents/ai/' . $fileName . '.docx';
        $fullPath = storage_path('app/public/' . $filePath);

        $dir = dirname($fullPath);
        if (!file_exists($dir)) {
            mkdir($dir, 0755, true);
        }

        $objWriter = IOFactory::createWriter($phpWord, 'Word2007');
        $objWriter->save($fullPath);

        return url('storage/' . $filePath);
    }

    private function generatePdf(array $data, string $fileName): string
    {
        $pdf = Pdf::loadView('pdf.ai-document-beautiful', ['data' => $data]);

        $filePath = 'documents/ai/' . $fileName . '.pdf';
        $fullPath = storage_path('app/public/' . $filePath);

        $dir = dirname($fullPath);
        if (!file_exists($dir)) {
            mkdir($dir, 0755, true);
        }

        $pdf->save($fullPath);

        return url('storage/' . $filePath);
    }
